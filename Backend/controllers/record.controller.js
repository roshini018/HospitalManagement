const downloadRecord = async (req, res) => {
  try {
    const record = await HealthRecord.findById(req.params.id);

    if (!record) {
      return res.status(404).json({ message: "Record not found in database" });
    }

    // 👉 ALWAYS extract filename (handles old + new DB values)
    const fileName = record.filePath.split(/[\\/]/).pop();

    // 👉 ABSOLUTE PATH FROM PROJECT ROOT
    const filePath = path.resolve(process.cwd(), "uploads", "records", fileName);

    console.log("👉 Trying path:", filePath);

    // 👉 DEBUG: show files actually present
    const folder = path.resolve(process.cwd(), "uploads", "records");
    console.log("👉 Files in folder:", fs.existsSync(folder) ? fs.readdirSync(folder) : "NO FOLDER");

    if (!fs.existsSync(filePath)) {
      return res.status(404).json({
        message: "Report file not found on server. Please contact support."
      });
    }

    return res.download(filePath, record.fileName);

  } catch (error) {
    console.error(error);
    res.status(500).json({ message: "Download failed" });
  }
};
const shareRecord = async (req, res, next) => {
  try {
    const { doctorEmail } = req.body;
    if (!doctorEmail) return res.status(400).json({ success: false, message: 'Doctor email is required' });

    const doctorUser = await User.findOne({ email: doctorEmail.toLowerCase(), role: 'doctor' });
    if (!doctorUser) return res.status(404).json({ success: false, message: 'Doctor with this email not found' });

    const record = await HealthRecord.findById(req.params.id);
    if (!record) return res.status(404).json({ success: false, message: 'Record not found' });

    if (record.patient.toString() !== req.user._id.toString()) {
      return res.status(403).json({ success: false, message: 'Only the patient can share this record' });
    }

    // FIX: Initialize sharedWith if it's null/undefined before pushing
    if (!record.sharedWith) {
      record.sharedWith = [];
    }

    if (!record.sharedWith.some(id => id.toString() === doctorUser._id.toString())) {
      record.sharedWith.push(doctorUser._id);
      await record.save();
    }

    res.json({ success: true, message: `Record shared with Dr. ${doctorUser.name}`, record });
  } catch (error) {
    next(error);
  }
};

const deleteRecord = async (req, res, next) => {
  try {
    const record = await HealthRecord.findById(req.params.id);
    if (!record) return res.status(404).json({ success: false, message: 'Record not found' });

    if (record.patient.toString() !== req.user._id.toString()) {
      return res.status(403).json({ success: false, message: 'Not authorized to delete this record' });
    }

    const filePath = path.resolve(__dirname, '../uploads/records', record.filePath);
    if (fs.existsSync(filePath)) fs.unlinkSync(filePath);

    await record.deleteOne();
    res.json({ success: true, message: 'Record deleted successfully' });
  } catch (error) {
    next(error);
  }
};

